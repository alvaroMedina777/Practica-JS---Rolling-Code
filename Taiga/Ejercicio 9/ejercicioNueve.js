// Utiliza un ciclo for para sumar los números del 1 al 10 y mostrar el resultado final.

// ALGORITMO
// Entrada
//     numeros del 1 al 10
// Proceso
//     Sumar los numeros del 1 al 10
// Salida
//     Mostrar resultado de la suma

let suma = 0;

for (let i = 1; i <= 10; i++) {
    suma = suma + i;
}

console.log(`La suma total de los numeros es: ${suma}`)
